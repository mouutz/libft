#include "libft.h"

void	*ft_memccpy(void *restrict d, const void *restrict s, int c, size_t n)
{
	unsigned char *dst;
	unsigned char *src;

	dst = (unsigned char *)d;
	src = (unsigned char *)s;
	while (n--)
	{
		if (*src == (unsigned char)c)
		{
			*dst++ = *src;
			return (dst);
		}
		*dst++ = *src++;
	}
	return (NULL);
}
